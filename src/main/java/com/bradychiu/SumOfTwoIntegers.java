package com.bradychiu;

public class SumOfTwoIntegers {
    /**
     * TODO: review bitwise operators
     *
     * XOR + Zero-fill left shift
     *
     *  1 : 0001
     *  2 : 0010
     * ---------
     *  3 : 0011
     *
     * 0001 ^ 0010 = 0011   0001 & 0010 = 0000  0000 << 1 = 0000
     *
     *  1 : 0001
     *  3 : 0011
     * ---------
     *  4 : 0100
     *
     *  0001 ^ 0011 = 0010   0001 & 0011 = 0001  0001 << 1 = 0010
     *  0010 ^ 0010 = 0000   0010 & 0010 = 0010  0010 << 1 = 0100
     *  0000 ^ 0100 = 0100   0000 & 0100 = 0000  0000 << 1 = 0000
     *
     * -1 : 1111
     *  1 : 0001
     *  --------
     *  0 : 0000
     *
     *  1111 ^ 0001 = 1110  1111 & 0001 = 0001  0001 << 1 = 0010
     *  1110 ^ 0010 = 1100  1110 & 0010 = 0010  0010 << 1 = 0100
     *  1100 ^ 0100 = 1000  1100 & 0100 = 0100  0100 << 1 = 1000
     *  1000 ^ 1000 = 0000  1000 & 1000 = 1000  1000 << 1 = 0000
     *
     *  -2 : 1110
     *   4 : 0100
     *  ---------
     *   2 : 0010
     *
     *  1110 ^ 0100 = 1010  1110 & 0100 = 0100  0100 << 1 = 1000
     *  1010 ^ 1000 = 0010  1010 & 1000 = 1000  1000 << 1 = 0000
     */
    public static int getSum(int a, int b) {
        while (b != 0) {
            int answer = a ^ b;
            int carry = (a & b) << 1;
            a = answer; b = carry;
        }
        return a;
    }
}
